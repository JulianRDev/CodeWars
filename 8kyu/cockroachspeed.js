// function that takes roach speed in km and returns it in cm per second

function cockroachSpeed(s) {
    //Good Luck!
    
    // find equation for km to cm per second
     // return equation that turned km per to cm per second
    
    // return equation floored to round down to integer
    return Math.floor(s * 27.777778)
  }